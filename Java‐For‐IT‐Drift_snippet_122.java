// if-setning
int tall = 7;

if (tall > 5) {
    System.out.println("Tallet er større enn 5.");
} else {
    System.out.println("Tallet er mindre enn eller lik 5.");
}

// for-løkke
for (int i = 0; i < 5; i++) {
    System.out.println("i er: " + i);
}

// while-løkke
int i = 0;
while (i < 5) {
    System.out.println("i er: " + i);
    i++;
}

// do-while-løkke
int j = 0;
do {
    System.out.println("j er: " + j);
    j++;
} while (j < 5);