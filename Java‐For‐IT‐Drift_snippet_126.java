public class MathExample {
    public static void main(String[] args) {
        double a = 16;
        double b = 9;
        System.out.println("Kvadratrot av " + a + " er " + Math.sqrt(a));
        System.out.println("Maksimum av " + a + " og " + b + " er " + Math.max(a, b));
        System.out.println("Minimum av " + a + " og " + b + " er " + Math.min(a, b));
    }
}