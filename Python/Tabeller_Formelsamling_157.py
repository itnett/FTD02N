# Kommutativ lov (addisjon og multiplikasjon)
a = 5
b = 3

print("a + b =", a + b)  # Output: 8
print("b + a =", b + a)  # Output: 8
print("a * b =", a * b)  # Output: 15
print("b * a =", b * a)  # Output: 15

# Assosiativ lov (addisjon og multiplikasjon)
c = 2

print("(a + b) + c =", (a + b) + c)  # Output: 10
print("a + (b + c) =", a + (b + c))  # Output: 10
print("(a * b) * c =", (a * b) * c)  # Output: 30
print("a * (b * c) =", a * (b * c))  # Output: 30

# Distributiv lov
print("a * (b + c) =", a * (b + c))  # Output: 25
print("a * b + a * c =", a * b + a * c)  # Output: 25